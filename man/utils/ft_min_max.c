#include "../push_swap.h"

void	ft_min_max(long *tab, int n)
{
	int	i;

	i = 0;
	while (i < n)
	{
		if (tab[i] > 2147483647 || tab[i] < -2147483648)
		{
			ft_putendl_fd("Error", 2);
			free(tab);
			exit(1);
		}
		i++;
	}
}
