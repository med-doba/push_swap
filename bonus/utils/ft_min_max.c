#include "../push_swap_bonus.h"

void	ft_min_max(t_var *my)
{
	int	i;

	i = 0;
	while (i < my->n)
	{
		if (my->tab_a[i] > 2147483647 || my->tab_a[i] < -2147483648)
		{
			ft_putendl_fd("Error", 2);
			free(my->tab_a);
			free(my);
			exit(1);
		}
		i++;
	}
}
