#include "../push_swap.h"

int	ft_order(int *nbr, int n)
{
	int i;

	i = 0;
	while (i < n - 1)
	{
		if (nbr[i] < nbr[i + 1])
			i++;
		else
			return (1);
	}
	return (0);
}
